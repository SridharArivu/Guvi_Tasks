public class Person {
    Integer id;
    String Name;
    int Age;

    public Person(Integer id, String name, Integer age) {
        this.id = id;
        Name = name;
        Age = age;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return Name;
    }
    public Integer getAge() {
        return Age;
    }
    

}
